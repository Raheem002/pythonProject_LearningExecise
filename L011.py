# Arthematic Operators
# +, -, *, /, %

a = 20
b = 60
print(a + b)
print(a - b)
print(a * b)
print(a / b)  # in division the result will always float in response.

print(a % b)  # modulus oparetor (it always retunrs in Reminders)
#     60 | 20 | 0.3
#        |200|
#        |180|
#        |20| so reminder is "20"

r = pow(a , 2) # Nothing but it acts as power 20 x 20 = 400
print(r) # response is 400

t = a // 5
print(t) # Response will be quotient
